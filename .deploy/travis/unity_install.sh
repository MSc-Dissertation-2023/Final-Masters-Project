#! /bin/sh

unity_version=$1
unity_version_url=$2

echo "------------------------------------------------------------------------------------------------------------------------"
echo "Downloading Unity $unity_version; ------------------------------------------------------------------------------------------"
echo "------------------------------------------------------------------------------------------------------------------------"
curl -o .deploy/Unity.pkg "$unity_version_url"

echo "------------------------------------------------------------------------------------------------------------------------"
echo 'Installing Unity; ------------------------------------------------------------------------------------------------------'
echo "------------------------------------------------------------------------------------------------------------------------"
if [ "$verbose" == "True" ];
then
    sudo installer -dumplog -package .deploy/Unity.pkg -target /
else
    sudo installer -package .deploy/Unity.pkg -target /
fi
